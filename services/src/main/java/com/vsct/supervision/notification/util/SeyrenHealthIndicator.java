package com.vsct.supervision.notification.util;

import java.io.IOException;
import java.net.InetSocketAddress;
import java.net.Socket;
import java.net.URI;

import org.springframework.beans.factory.annotation.Value;
import org.springframework.boot.actuate.health.AbstractHealthIndicator;
import org.springframework.boot.actuate.health.Health;
import org.springframework.stereotype.Component;

@Component
public class SeyrenHealthIndicator extends AbstractHealthIndicator {

    @Value("${seyren.host}")
    private String seyrenUrl;

    @Override
    protected void doHealthCheck(Health.Builder builder) throws Exception {
        URI uri = new URI(seyrenUrl);

        if (pingHost(uri.getHost(), uri.getPort(), 2000)) {
            builder.up();
        } else {
            builder.down();
        }
    }

    public static boolean pingHost(String host, int port, int timeout) {
        try (Socket socket = new Socket()) {
            socket.connect(new InetSocketAddress(host, port), timeout);
            return true;
        } catch (IOException e) {
            return false; // Either timeout or unreachable or failed DNS lookup.
        }
    }
}
