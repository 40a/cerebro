package com.vsct.supervision.seyren.api;

public enum AlertType {

    UNKNOWN(0), OK(1), WARN(2), ERROR(3), EXCEPTION(4);

    private final int value;

    private AlertType(final int value) {
        this.value = value;
    }

    public boolean isWorseThan(final AlertType other) {
        if (other == null) {
            return true;
        }
        return value > other.value;
    }

}
